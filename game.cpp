// Checks win condition for both player and goblin
bool Goblin_Slayer::win_condition(const int& goblin_Hp, const int& player_Hp,const std::string& player_name)
{
    if (goblin_Hp <= 0) // If goblin HP = 0 or less than 0, Player win!
    {
        system("clear");
        picture_player_wins(player_name,player_Hp);
        pause();
        return true; 
    }
    else if (player_Hp <= 0) // If player HP = 0 or less than 0, Goblin win!
    {
        system("clear");
        picture_goblin_wins(player_name, goblin_Hp);
        pause();
        return true;
    }
    return false;
}

