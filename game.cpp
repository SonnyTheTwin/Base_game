std::string Goblin_Slayer::choose_character_name(Player& p1)
{
    std::string Player_Name;
    system("clear");
    picture_choose_name(); // Background pic 
    Player_Name = p1.SetName(); // call player memberfunction = SetName();
    return Player_Name;
}

char Goblin_Slayer::character_choice()
{
    char input;
    system("clear");
    picture_battle_choices(); // Backgroound pic
    std::cin >> input; // Get users choice
    return input;
}

int Goblin_Slayer::player_attack(Player& p1, Goblin& g1, const std::string& player_name, int& goblin_current_hp)
{
    system("clear");
    int player_Dmg = p1.Take_Dmg(); // Calls for player memberfunction Take_Dmg to fetch player dmg
    goblin_current_hp = g1.Set_Hp(player_Dmg); // sets goblin Hp after calling goblin memberfunction Set_Hp with players dmg. This so we can later send goblin_current_hp to our bool
    picture_player_attack(player_name,player_Dmg, goblin_current_hp); // background pic
    pause();
    return goblin_current_hp; // returns goblins current hp
}

int Goblin_Slayer::goblin_attack(Goblin& g1, Player& p1, std::string& player_name, int& player_current_hp)
{
    system("clear");
    int goblin_dmg = g1.Take_Dmg(); // Calls for goblin memberfunction Take_Dmg and sets dmg 
    player_current_hp = p1.Set_Hp(goblin_dmg); // sets player hp after calling player memberfunction Set_Hp
    picture_goblin_attack(player_name,goblin_dmg,player_current_hp);
    pause();
    return player_current_hp; 
}

/* THE GAME */
void Goblin_Slayer::game_loop()
{
    std::string user_input;
    filehandler New_Save;

    int exit = 0;
    do
    {
        user_input=main_menue();
        Goblin g1;
        Player p1;

        switch (user_input[0])
        {
        case '1':
            {
            std::string player_name = choose_character_name(p1);
            char input;
            int goblin_current_hp = 100; // doesnt have to be here, let be for now
            int player_current_hp = 100;

            std::stringstream Data_To_Save = p1.Save_Entity();
            New_Save.write_to_file(Data_To_Save);
            //pause();
            do
            {
                input = character_choice();
                switch(input)
                {
                case '1':  // Player attack, then Goblin Attack
                    goblin_current_hp = player_attack(p1,g1,player_name, goblin_current_hp);
                    pause();
                    if (win_condition(goblin_current_hp,player_current_hp, player_name))
                    {
                        pause();
                        input = '3';
                        break;
                    }

                    player_current_hp = goblin_attack(g1,p1,player_name, player_current_hp);
                    if (win_condition(goblin_current_hp,player_current_hp,player_name))
                    {
                        //pause();
                        input = '3';
                        break;
                    }
                    break;
                case '2': // ONLY GOBLIN DOES DMG
                    player_current_hp = goblin_attack(g1,p1,player_name, player_current_hp);
                    pause();
                    if (win_condition(goblin_current_hp,player_current_hp,player_name))
                    {
                        //pause();
                        input = '3';
                        break;
                    }
                    //pause();
                    break;
                case '3': // FLEE == EXIT BACK TO MAIN MENUE
                    /* Exit Program */
                    pause();
                    break; 
                default:
                    system("clear");
                    picture_Error();
                    pause();
                    pause();    
                }         
            }while (input != '3');
        }
            break;
        case '2':
            exit = 1;
            break;
        default:
            system("clear");
            picture_Error();
            pause();
            break;
        }
    //std::cin.get();
    } while (exit != 1);
}
